<?php
namespace App\Services;


class CurlService
{
    private static $apiUrl = CURLPAI;
    private static $cryptToken = CRYPTTOKEN;
    private static $curlGuid = CURLGUID;

    /**
     * CURLf方法
     * @param $url
     * @param bool $param
     * @param int $ispost
     * @param int $https
     * @return bool|mixed
     */
    public static function curl($url, $param = false, $ispost = 1, $https = 0)
    {
        $httpInfo = array();
        $ch = curl_init();
        curl_setopt($ch, CURLOPT_HTTP_VERSION, CURL_HTTP_VERSION_1_1);
        curl_setopt($ch, CURLOPT_USERAGENT, 'Mozilla/5.0 (Windows NT 10.0; WOW64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/41.0.2272.118 Safari/537.36');
        curl_setopt($ch, CURLOPT_CONNECTTIMEOUT, 30);
        curl_setopt($ch, CURLOPT_TIMEOUT, 30);
        curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
        if ($https) {
            curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, FALSE); // 对认证证书来源的检查
            curl_setopt($ch, CURLOPT_SSL_VERIFYHOST, FALSE); // 从证书中检查SSL加密算法是否存在
        }

        $url = self::$apiUrl . $url;
        $cryptToken = self::$cryptToken;
        $time = time();
        $guid = self::$curlGuid;
//        $group_guid = GROUP_GUID;


        $param = json_encode($param);
        $signature = md5(md5($time . $guid . $param . $cryptToken), '39ad5014fc77197034142f8a45c4b966');
        $data = array(
            'signature' => $signature,
            'param' => $param,
            'time' => $time,
            'guid' => $guid,
        );
        if ($ispost) {
            curl_setopt($ch, CURLOPT_POST, true);
            curl_setopt($ch, CURLOPT_POSTFIELDS, $data);
            curl_setopt($ch, CURLOPT_URL, $url);
        } else {
            if ($data) {
                if (is_array($data)) {
                    $data = http_build_query($data);
                }
                curl_setopt($ch, CURLOPT_URL, $url . '?' . $data);
            } else {
                curl_setopt($ch, CURLOPT_URL, $url);
            }
        }

        $response = curl_exec($ch);

        if ($response === FALSE) {
            //echo "cURL Error: " . curl_error($ch);
            return false;
        }
        $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
        $httpInfo = array_merge($httpInfo, curl_getinfo($ch));
        curl_close($ch);
        return $response;
    }


